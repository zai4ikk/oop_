/* Использование деструктора для освобождения ресурсов
Условие задачи: Создайте класс ResourceHandler, который захватывает и освобождает ресурсы. 
Например, это может быть любой ресурс, например, соединение с базой данных, блокировка мьютекса и т.д.
В этом классе реализуйте деструктор для корректного освобождения ресурса.
*/

#include <iostream>
using namespace std;

class ResourceHandler {
private:
    bool resourceAcquired;

public:
    // Конструктор захватывает ресурс
    ResourceHandler() : resourceAcquired(false) {
        // Симуляция захвата ресурса
        resourceAcquired = true;
        cout << "Ресурс захвачен." << endl;
    }

    // Деструктор освобождает ресурс
    ~ResourceHandler() {
        if (resourceAcquired) {
            // Симуляция освобождения ресурса
            cout << "Ресурс освобожден." << endl;
        }
    }
};

int main() {
    {
        ResourceHandler handler;  // Захватываем ресурс
        // Ресурс будет освобожден при выходе из области видимости объекта
    }  // Объект выходит из области видимости и срабатывает деструктор
    return 0;
}
