/******************************************************************************
Задача: Напишите программу, которая запрашивает у пользователя 
ввод двух чисел и делит первое число на второе. 
Обработайте ситуацию, когда пользователь вводит ноль 
в качестве делителя, предотвращая ошибку выполнения.
*******************************************************************************/
#include <iostream>
using namespace std;

int main() {
    double numerator, denominator, result;

    cout << "Введите числитель: ";
    cin >> numerator;
    cout << "Введите знаменатель: ";
    cin >> denominator;

    if (denominator == 0) {
        cout << "Ошибка: деление на ноль невозможно." << endl;
    } else {
        result = numerator / denominator;
        cout << "Результат: " << result << endl;
    }

    return 0;
}

